		<div> <!-- Detailed Information -->
			<h2><img src="./img/slicedice/icon_pizza.png" class="h-icon" alt="Pizza icon"> The Concept</h2>
			<hr class="uk-divider-small">
			<p>
				Encouraging engagement and fun, Slice & Dice turns a pizza box into a board game mixed with the party drink of choice, alcohol. Local pizzerias geared towards college students and well-known tabletop games were used as references when designing. The boxes were <span class="uk-text-bold">screen printed</span> and assets such as a rules sheet and stickers accompanied the package.
			</p>
			<h2><img src="./img/slicedice/icon_dice.png" class="h-icon" alt="Dice icon"> The Rules</h2>
			<hr class="uk-divider-small">
			<p>
				Players traverse a board reaching each minigame spot where players complete in a group drinking game. Go through each minigame spot once to win. Three games included are based off commonly played drinking games: King's Cup (Ring of Fire), Never Have I Ever, and Two Truths One Lie. The fourth is similar to a classic drawing game.
			</p>
			<hr>
			<p class="uk-text-center">
				<span class="uk-text-bold">Project Scope</span>
				<ul class="uk-list uk-list-bullet">
					<li>Create a game and rules to engage players</li>
					<li>Design a logo, rules sheet, multiple boards, and more</li>
					<li>Illustrate icons in relation to design choice</li>
					<li>Utilize screen printing to execute the concept</li>
				</ul>
			</p>
