          High-dose chemotherapy/radiation therapy and stem cell transplant
        </a>
      </b></h5>
    </div>
    <div id="collapseFive" class="collapse" role="tabpanel" aria-labelledby="headingFive" data-parent="#accordion">
      <div class="card-body">
<p>This type of treatment is used in children with high-risk neuroblastoma who are unlikely to be cured with other treatments. It involves giving high doses of chemotherapy (higher than could safely given otherwise) and/or radiation therapy (total body irradiation or high-dose MIBG), and then replacing the body’s bone marrow cells, which were killed by the treatment. In the past, this type of treatment was commonly referred to as a bone marrow transplant. </p>

<p>The bone marrow is the soft, inner part of some bones where new red blood cells, white blood cells, and platelets are formed. Red blood cells carry oxygen to all parts of the body. White blood cells are part of the immune system, which fights off infections. Platelets are needed to stop bleeding caused by cuts and scrapes. </p>

<p>Both chemotherapy and some types of radiation can affect blood-forming stem cells in the bone marrow. (These are the cells that make the different types of blood cells.) Even though more intensive treatments might be more effective in treating tumors, they can’t be given because they would cause severe damage to the bone marrow, leading to life-threatening shortages of blood cells. </p>

<p>Doctors try to get around this problem by giving the child an infusion of blood-forming stem cells after treatment. This is known as a peripheral blood stem cell transplant (PBSCT). </p>
<h5>What It Involves</h5>
<p>The first step in a PBSCT is to collect, or “harvest,” the child’s own blood-producing stem cells to use later. In the past, the stem cells were often taken from the child’s bone marrow, which was done by drilling small holes in certain bones. But doctors have found that these cells can be taken from the bloodstream during a procedure known as apheresis. This is similar to donating blood, but instead of going into a collecting bag, the blood goes into a special machine that filters out the stem cells and returns the other parts of the blood back to the person’s body. The stem cells are then frozen until the transplant. </P>

<p>After the harvest, the child gets high-dose chemotherapy and/or radiation. When treatment is complete, the patient’s stem cells are thawed and returned to the body in a process similar to a normal blood transfusion. The stem cells travel through the bloodstream and settle in the bone marrow. Over the next 3 or 4 weeks, the stem cells start to make new, healthy blood cells in the child’s bone marrow. </p>

<p>Until this happens, the child is at high risk of infection because of a low white blood cell count, as well as bleeding because of a low platelet count. To avoid infection, protective measures are taken, such as using special air filters in the hospital room and having visitors wear protective clothing. Blood and platelet transfusions and treatment with IV antibiotics may also be used to prevent or treat infections or bleeding problems. </P>

<p>A peripheral blood stem cell transplant is a complex treatment. If the doctors think your child may benefit from a transplant, the best place to have this done is at a nationally recognized cancer center where the staff has experience in performing the procedure and managing the recovery period. </p>

<p>A stem cell transplant is also very expensive and often requires a lengthy hospital stay. Because the procedure is so expensive, you should have an idea of how the costs might be covered beforehand. Be sure to get a written approval from your insurer if the procedure is recommended for your child. </p>

<h5>Possible Side effects</h5>
<p>Possible early complications and side effects are basically the same as those caused by any other type of high-dose chemotherapy or radiation therapy, and are due to damage to the bone marrow and other quickly dividing tissues of the body. They can include low blood cell counts (with increased risk of infection and bleeding), nausea, vomiting, loss of appetite, mouth sores, and hair loss. </P>

<p>One of the most common and serious short-term effects is an increased risk for infection. Antibiotics are often given to try to prevent this from happening. Other side effects, like low red blood cell and platelet counts, may require blood product transfusions or other treatments. </P>

<p>Some complications and side effects can persist for a long time or may not occur until years after the transplant. Be sure to talk to your child’s doctor before the transplant to learn about possible long-term effects your child may have.</p>       </div>
