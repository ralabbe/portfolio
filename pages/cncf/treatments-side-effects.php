        <p>Retinoids are chemicals that are related to vitamin A. They are known as differentiating agents because they are thought to help some cancer cells to mature (differentiate) into normal cells. </p>

<p>In children with high-risk neuroblastoma, treatment with a retinoid called 13-cis-retinoic acid (isotretinoin) reduces the risk of recurrence after high-dose chemotherapy and stem cell transplant. Most doctors now recommend 6 months of 13-cis-retinoic acid (taken as a capsule) once therapy is completed. </P>

<p>Researchers are now trying to develop more effective retinoids and to define the exact role of this approach in the treatment of neuroblastoma.</p>

<h5>Possible Side Effects: </h5>
<p>The most common side effect of 13-cis-retinoic acid is drying and cracking of the lips. Dry skin or eyes are also possible, as are nosebleeds and changes in the nails.</p>
