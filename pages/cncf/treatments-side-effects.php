    <div id="collapseOne" class="collapse show" role="tabpanel" aria-labelledby="headingOne" data-parent="#accordion">
      <div class="card-body">
      	<p>Surgery can be used both to help diagnose neuroblastoma and to treat it. For smaller tumors that have not spread, surgery is often the only treatment that is needed.</p> 
<h5><b>Surgical (open) biopsy</b></h5>
<p>Before treatment begins, doctors may do a surgical biopsy to remove tumor samples to be looked at under a microscope and for other lab tests. If the tumor is in the abdomen, the surgeon may do the biopsy with the aid of a laparoscope. This is a long, thin tube with a tiny video camera on the end. It is inserted into the abdomen through a small incision to allow the surgeon to see inside. The surgeon then makes a second small incision to reach inside the abdomen with long, thin instruments and remove a small piece of tumor. </p>
<h5><b>Surgery as treatment</b></h5>
<p>After neuroblastoma is diagnosed, surgery is often used to try to remove as much of the tumor as possible. In some cases, surgery can remove the entire tumor and bring about a complete cure. During the operation, the surgeon looks carefully for signs of tumor spread to other organs. Nearby lymph nodes are removed and looked at under a microscope for cancer cells. If possible, the surgeon will remove the entire tumor. This is less likely to be possible if the tumor is wrapped around large blood vessels. Even if the tumor cannot be completely taken out, treatment with chemotherapy (and sometimes radiation therapy) after removing most of the cancer may result in a cure. Sometimes surgery is repeated after other treatments (chemotherapy and/or radiation therapy) to check the results of therapy and to remove any remaining cancer if possible. If the tumor is very big, chemotherapy may be used before surgery. This can shrink the tumor and make it easier to remove completely.</p> 
<h5><b>Possible risks and side effects of surgery</b></h5>
<p>Like all forms of treatment, surgery poses some risk of complications. These can include reactions to anesthesia, excess bleeding, and damage to blood vessels, kidneys, other organs, or nerves. Most complications are minor, but serious ones are possible. Complications are more likely if the tumor is large and growing into blood vessels or nerves.</p>
