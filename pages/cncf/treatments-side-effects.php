    <div id="collapseFour" class="collapse" role="tabpanel" aria-labelledby="headingFour" data-parent="#accordion">
      <div class="card-body">
<p>Radiation therapy uses high-energy rays or particles to kill cancer cells. Radiation is sometimes a necessary part of treatment, but because of the possible long-term side effects in children, it is avoided when possible. Two types of radiation therapy can be used to treat children with neuroblastoma.</P>
<h5>External Beam Radiation Therapy</h5>
<p>This type of radiation therapy focuses radiation on the cancer from a source outside the body. There are several situations in which this type of radiation therapy might be used: </p>
<ul style="list-style-type: disc; width: 80%; margin: 0 auto;">
  <li>To destroy neuroblastoma cells that remain behind after surgery and chemotherapy</li>
  <li>To try to shrink tumors before surgery, making them easier to remove at the time of surgery</li>
  <li>To treat larger tumors that are causing serious problems (such as trouble breathing) and do not respond quickly to chemotherapy</li>
  <li>As part of the treatment regimen (along with high-dose chemotherapy) before a stem cell transplant in children with high-risk neuroblastoma</li>
  <li>To help relieve pain caused by advanced neuroblastoma</li>
</UL><BR />
<p>The radiation is typically directed at the tumor itself, but in some cases it may also target other parts of the body to reduce the risk of cancer spread. When radiation is delivered throughout the body, it is known as total body irradiation (TBI). External radiation therapy is much like getting an x-ray, but the radiation is more intense. The procedure itself is painless. </p>

<p>Before treatments start, the radiation team takes careful measurements with imaging tests such as MRI scans to determine the correct angles for aiming the radiation beams and the proper dose of radiation. Each actual treatment lasts only a few minutes, but the setup time – getting your child into place for treatment – usually takes longer. Young children may be given medication to make them fall asleep so they will not move during the treatment. Most often, radiation treatments are given 5 days a week for several weeks. </p>

<h5>Possible Side Effects</h5>
<p>Short-term side effects of external radiation therapy may include mild skin reactions, nausea, diarrhea, or fatigue. Often these go away after a short while. Talk with your child’s doctor about these side effects because there are ways to relieve some of them. Radiation may also make the side effects of chemotherapy worse. </p>
<p>Radiation therapy can interfere with the growth of normal body tissues, and may increase the risk of developing other cancers later on. The actual effects depend on the part of the body getting radiation.</p> 
<h5>MIBG radiotherapy</h5>
<p>MIBG is a chemical similar to norepinephrine, which is made by sympathetic nerve cells. A slightly radioactive form of MIBG is sometimes injected into the bloodstream as part of an imaging test to look for neuroblastoma cells in the body. </p>

<p>A more highly radioactive form of MIBG is also used to treat some patients with advanced neuroblastoma, often along with other treatments. Once injected into the bloodstream, the MIBG goes to the sites of tumors anywhere in the body, where it delivers its radiation. The child will need to stay in a special room for a few days after the injection until most of the radiation has left the body. </p>

<h5>Possible Side Effects</h5>
<p>MIBG therapy can sometimes cause nausea and vomiting. It can also lower blood cell counts because of its effects on the bone marrow. In rare cases it may cause high blood pressure for a short period of time. MIBG may build up in the thyroid gland in the neck, which can sometimes result in low levels of thyroid hormone in the body.</P>      </div>
