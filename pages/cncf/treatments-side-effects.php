          Chemotherapy
        </a>
      </b></h5>
    </div>
    <div id="collapseTwo" class="collapse" role="tabpanel" aria-labelledby="headingTwo" data-parent="#accordion">
      <div class="card-body">
      	<p>Chemotherapy is the use of anti-cancer drugs. They are most often administered by IV or directly into a vein. The drugs enter the bloodstream and circulate throughout the body to reach and destroy cancer cells. This makes chemotherapy useful for treating neuroblastoma that has spread to the lymph nodes, bone marrow, liver, lungs, or other organs. </p>

<p>Some cases of neuroblastoma are treated with chemotherapy along with surgery, either before (neoadjuvant chemotherapy) or after (adjuvant chemotherapy). In other cases, especially when the cancer has spread too far to be completely removed by surgery, chemotherapy is the primary treatment.</p>

<p>Most children with neuroblastoma will need chemotherapy. In most cases, chemotherapy treatment involves a combination of medicines. The main drugs used to treat children with neuroblastoma include:</p>
<ul style="list-style-type: disc; width: 80%; margin: 0 auto;">
	<li>Cyclophosphamide or ifosfamide</li>
	<li>Cisplatin or carboplatin</li>
	<li>Vincristine</li>
	<li>Doxorubicin (Adriamycin)</li>
	<li>Etoposide</li>
	<li>Topotecan</li>
</ul><br />
<p>The most common combination of drugs used to treat neuroblastoma consists of carboplatin (or cisplatin), cyclophosphamide, doxorubicin, and etoposide, but others may be used. For children in the high-risk group, larger combinations of drugs are used, and the drugs are given at higher doses, which may be followed by a stem cell transplant (see below). </p>

<h5>Possible side effects of chemotherapy </h5>
<p>Chemotherapy drugs work by attacking cells that are dividing quickly, making them effective against cancer cells. The downside But other cells in the body, such as those in the bone marrow, the lining of the mouth and intestines, and the hair follicles, also divide quickly. These cells are also likely to be affected by chemotherapy, which can lead to side effects. </p>

<p>Children seem to have an advantage over adults when it comes to chemotherapy. They tend to have less severe side effects and recover from side effects more quickly. One benefit of this is that doctors can give high doses of chemotherapy that are necessary to kill the tumor. </p>

<p>The side effects of chemotherapy depend on the type and dose of drugs given and the length of time they are taken. General side effects of chemotherapy drugs can include:</P>
<ul style="list-style-type: disc; width: 80%; margin: 0 auto;">
	<li>Hair loss</li>
	<li>Mouth sores</li>
	<li>Loss of appetite</li>
	<li>Nausea and vomiting</li>
	<li>Increased chance of infections (due to low white blood cell counts)</li>
	<li>Easy bruising or bleeding (due to low blood platelet counts)</li>
	<li>Fatigue (due to low red blood cell counts)</li>
</ul><br />

<p>Most side effects are short-term and disappear after treatment is completed. In addition, many side effects can be lessened through a number of techniques. There are ways to lessen many common treatment side effects. For example, drugs can be given to help prevent or reduce nausea and vomiting. Be sure to discuss any questions you have about side effects with the cancer care team.</p>

<p>Along with the possible side effects listed above, some side effects are specific to certain medicines: <p>

<p>Cyclophosphamide and ifosfamide can damage the bladder. This can be avoided or minimized by giving the drugs with plenty of fluids and with a drug called mesna, which helps protect the bladder. These drugs can also damage the ovaries or testicles and may affect fertility (the ability to have children). </p>

<p>Doxorubicin can cause heart damage. Doctors try to reduce this risk as much as possible by not giving more than the recommended doses of doxorubicin and by checking the heart with a test called an echocardiogram (an ultrasound of the heart) during treatment. This drug can also cause skin damage if it should leak out of the vein during administration. </p>

<p>Cisplatin and carboplatin can affect the kidneys. Giving plenty of fluids can help reduce this risk. These drugs can also affect hearing in some cases. Your child's doctor may check this with hearing tests (audiograms) during or after treatment. </p>

<p>Vincristine can damage nerves. Some patients may notice tingling and numbness, particularly in the hands and feet. </p>

<p>Chemotherapy can also have longer-term side effects in some cases. For example, some drugs used to treat neuroblastoma can increase the risk of later developing a cancer of white blood cells known as acute myeloid leukemia (AML). While this is a serious risk, it is not common, and the importance of chemotherapy in treating neuroblastoma far outweighs this risk.</p>
