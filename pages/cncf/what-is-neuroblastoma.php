	<h2>What is Neuroblastoma?</h2>
	<p>Neuroblastoma is a solid tumor cancer that arises in immature nerve cells and strikes primarily infants and children. It is the most common cancer affecting infants with an incidence rate of almost double that of leukemia. Its cause is unknown. Nearly 70 percent of children diagnosed with Neuroblastoma have advanced-stage disease. Less than 40 percent of children with advanced disease live five years.<br /><br />

The cancer is a solid tumor-a lump or mass-originating from neural crest tissue that is part of the sympathetic nervous system (SNS). This part of the nervous system is responsible for the "fight or flight" response when stress occurs. Nerves of the sympathetic nervous system run parallel along the outside of the spinal column and connect to organs. Since neuroblastoma arises at the interface between the nervous system and the endocrine system (the hormone producing organs-it is one of the few cancers that secrete hormones), it is also included in the class of neuroendocrine tumors.<br /><br />

The most common place for neuroblastoma to originate is on the adrenal glands located above each kidney (40 percent of localized tumors and 60 percent of wide-spread disease). Neuroblastoma tumors can also develop in nerve tissues in the neck (1 percent), chest (19 percent), abdomen (30 percent non-adrenal), or pelvis (1 percent)-anywhere along the chain of the sympathetic nervous system. In rare cases, no primary tumor can be discerned.<br /><br />

"Neuro-" indicates origin in nerve cells, and "blast" means immature cells. Normal "neuroblasts" (baby nerve cells) begin in embryonic tissue and grow and mature into functioning nerve cells. Neuroblastoma means the immature cells reproduce forming a mass and do not develop into functioning cells (the "-oma" ending denotes a tumor). Neuroblastoma is not a cancer of the central nervous system (CNS) and it is not a brain cancer, but occasionally it metastasizes to the CNS. There are over 50 kinds of pediatric cancers that fall into 12 main categories, one of which is the sympathetic nervous system cancers. Neuroblastoma accounts for more than 97 percent of all sympathetic nervous system cancers.</p>
